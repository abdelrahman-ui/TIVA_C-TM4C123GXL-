 #ifndef BIT_MATH_H_
 #define BIT_MATH_H_


 #define SET_BIT(x, pos) (x |= (1U << pos))
 #define CLEAR_BIT(x, pos) (x &= (~(1U<< pos)))
 #define TOGGLE_BIT(x, pos) x ^= (1U<< pos)
 #define CHECK_BIT(x, pos) (x & (1UL << pos) )
 #define SWAP_NIBBLES(x) ((x & 0x0F)<<4 | (x & 0xF0)>>4)


 #endif /* BIT_MATH_H_ */
